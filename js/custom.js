function showLogin(){
    $(".fade").css("display", "block");
    $(".fade").css("opacity","1");
    $(".login-form").css("display", "block");
};

function closeLogin(){
    $(".fade").css("display", "none");
    $(".fade").css("opacity","0");
    $(".login-form").css("display", "none");
};